console.log(console);               // console object has various functions which are related to browser's console tab.

console.log("Hello, this is console.log() function")            // console.log() function is used to log something on the console.

console.error("!!! This is an Error !!!");                      // .error() function is used to display a error in console. Error can be seperately viewed in Error section of the console tab.

console.assert("Hello" == 50);                                  // .assert() returns nothing (i.e undefined) if assertion is true.
console.assert(100 >= 50);                                      // .assert() gives Error if assertion is false.