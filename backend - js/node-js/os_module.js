const os = require("os");       // os is a built-in module of node-js, it performs operations related to O.S

console.log(os.freemem());      // freemem() is a function of os module, which returns available System Memory (RAM).
console.log(os.homedir());      // homedir() is a function of os module, which returns the Home Directory of the Computer.
console.log(os.hostname());     // hostname() function returns the name of the system.
console.log(os.platform());     // platform() function returns the name of the os  pplatform.
console.log(os.release());      // release() function returns the release of the os.
console.log(os.type());         // release() function returns the release of the os.
console.log(os.totalmem());     // release() function returns the release of the os.
console.log(os.uptime());     // release() function returns the release of the os.
