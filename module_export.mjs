export function Hello(){                                                // export keyworrd is used to export ES6 modules.
    console.log("Hello, this is exported as ES6");
}

export function hi(){
    console.log("hi, this is exported as ES6");
}


export default function exp(){                                          // default keyword represents the modules that will be exported by-default
    console.log("Hello, this is exported as default");
}
